function buildSection(keys, values) {
  return Object.fromEntries(keys.map((key, index) => [key, values[index]]));
}

function buildLang(sectionValues) {
  return {
    ...buildSection(translationSections.common, sectionValues.common),
    ...buildSection(translationSections.alt, sectionValues.alt),
    ...buildSection(translationSections.home, sectionValues.home),
    ...buildSection(translationSections.footer, sectionValues.footer),
    ...buildSection(translationSections.accommodation, sectionValues.accommodation),
    ...buildSection(translationSections.gallery, sectionValues.gallery),
    ...buildSection(translationSections.location, sectionValues.location),
    ...buildSection(translationSections.contact, sectionValues.contact)
  };
}

